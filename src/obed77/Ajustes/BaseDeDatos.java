public void respaldo()
{
   int resp;
   ConexionBD con = new ConexionBD();
   String pass = con.getPass();
   String user = con.getUser();
   String bd= con.getDb();
   Calendar c=Calendar.getInstance();
   String fecha=String.valueOf(c.get(Calendar.DATE));
   fecha=fecha+"-"+String.valueOf(c.get(Calendar.MONTH));
   fecha=fecha+"-"+String.valueOf(c.get(Calendar.YEAR));
   
   resp=fc.showSaveDialog(this);
   if(resp==JFileChooser.APPROVE_OPTION)
   {
       try{
          Runtime rt = Runtime.getRuntime();
          File backupFile = new File(String.valueOf(fc.getSelectedFile().toString())+"_"+fecha+".sql");
          FileWriter fw = new FileWriter(backupFile);
          Process child = rt.exec(System.getProperty("user.dir")+
                  "\\mysqldump --opt --password="+pass+" --user="+user+" --databases "+bd+" -R");
          InputStreamReader irs = new InputStreamReader(child.getInputStream());
          BufferedReader br = new BufferedReader(irs);
          
          String line;
          while((line=br.readLine()) != null)
          {
              fw.write(line+"\n");
          }
          fw.close();
          irs.close();
          br.close();
       
       }catch(Exception e)
       {
           JOptionPane.showMessageDialog(null,"Error no se pudo generar el archivo"+ e);
       }
       JOptionPane.showMessageDialog(null, "Archivo generado");
   }else if (resp==JFileChooser.CANCEL_OPTION){
       JOptionPane.showMessageDialog(null, "Ha cancelado la operaci칩n");
   }
}

public void validar()
{
    if(rad_re.isSelected())
    {
        respaldo();
    }
    else
    {
        restaurar();
    }
}
  public void restaurar()
  {
      ConexionBD con = new ConexionBD();
        String pass = con.getPass();
        String user = con.getUser();
        String bd= con.getDb();
      int resp;
        resp=fc.showOpenDialog(this);
        if (resp==JFileChooser.APPROVE_OPTION) {
        try{
        String ubicacion= String.valueOf(fc.getSelectedFile ());
        Process child = Runtime.getRuntime().exec(System.getProperty("user.dir")+
                  "\\mysql --user="+user+" --password="+pass+" "+bd);
        
        
        OutputStream os = child.getOutputStream();
        FileInputStream fis = new FileInputStream(ubicacion);
        
        byte buffer[] = new byte[1024];
        int leido = fis.read(buffer);
        while(leido>0){
            System.out.println(leido);
            os.write(buffer,0,leido);
            leido= fis.read(buffer);
        }
        os.close();
        fis.close();
    
        JOptionPane.showMessageDialog(null,"Se ha Actualizado la Base de Datos","Respaldo",JOptionPane.INFORMATION_MESSAGE) ;
        }catch(Exception e){
        JOptionPane.showMessageDialog(null, "No se ha actualizado la Base de Datos: " + e.getMessage(), "Respaldor",JOptionPane.ERROR_MESSAGE);
        }
        }else{
        JOptionPane.showMessageDialog(null,"Ha cancelado la actualizaci칩n");
        }
  }
